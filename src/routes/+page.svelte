		<h2>A CS Major</h2>
		<blockquote>
			<p>My desired major remains computer science. I simply love programming, and it just makes me whole. Not that programming is everything there is to computer science!—but the ability to problem-solve, to apply my pattern recognition, to think like a computer, to create tools for my own ease of use, to build products and express myself, and the vast amount there is to learn simply sates all of my core needs.</p>

			<p>And for all that I already know, there is still so much more. To further my web design skills, there are a few goals I have: to learn a proper design tool, to learn a stylesheet language (like SCSS or Tailwind), to learn more deeply into building and architecting databases, to dig deeper into accessibility, and simply to build more and gain more experience. In doing so, I can push further to my end-goal of user-first design.</p>

			<p>I think the biggest attribute of successful UX designers is empathy: to be able to listen and understand the needs of the user. Which seems at odds with a lot of the stereotypes we hold about programmers, making it doubly important. A lot of developers can build interfaces that work, but making them both pretty and accessible is really difficult. Designing for screenreaders is an entire added layer of complexity that requires dedication and forethought.</p>

			<p>I think the path to learning web design is clear, but as far as computer science as whole goes, there are so many ways to succeed.</p>
		</blockquote>
