		<h2>In The Real World</h2>
		<blockquote>
			<p>As part of my focus on user-centric design, to me, being a successful engineer means making any project that I touch better for its users. There are too many websites that work but are horrible for the end-user. I like to pick on myUK, which… may not be fair given its age. But it is a prime target: the content layout with tab systems is awkward, it contains a number of embedded pages that you can't use the forward or back navigation buttons on, and it isn't keyboard accessible, let alone screenreader-accessible, just to name a few.</p>

			<p>I want to make products that are user-focused inside and out; that are both beautiful and accessible to people of all levels of access. Products that don't make people sigh that I guess that's the way it is. A professional position I seek is one that allows me to improve the hassle of daily life over and over. Ideally, my work would be completely unnoticed all together. I also hope to be constantly challenged; to never get bored of what I do, or get complacent, and even better to push me out of my comfort zone. A job that both terrifies me and excites me.</p>

			<p>In the following 10 years, I hope that I get to keep doing what I love, without ever feeling stagnant. The thing I fear the most is getting relegated to a managerial position where I no longer get to write code or solve problems.</p>
